#include <matrix.h>

t_mat4	mat4_sub_vec(t_mat4 matrix, t_vec3 sub)
{
	matrix.data[3][0] -= sub.x;
	matrix.data[3][1] -= sub.y;
	matrix.data[3][2] -= sub.z;
	return (matrix);
}
