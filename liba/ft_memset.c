#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	int	run;

	run = 0;
	while (n > 0)
	{
		((unsigned char *)b)[run] = ((unsigned char)c);
		run++;
		n--;
	}
	return (b);
}
