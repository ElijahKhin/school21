#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	runner;

	runner = 0;
	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (s[runner] != '\0')
		write(1, &s[runner++], 1);
	return (runner);
}
